#include <unistd.h>

int     main(int argc, char **argv)
{
	if (argc == 2)
	{
		while (*argv[1] != 'a') //||  *argv[1] != '\0')
			argv++;
		if (*argv[1] == 'a')
			write(1, "a", 1);
	}
	else
		write(1, "nihua", 5);
	write(1,"\n",1);
	return (0);
}
