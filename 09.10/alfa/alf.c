#include <unistd.h>

int		main()
{
	char str[26];
	int i = 0;
	char j = 'a';
	char g = 'B';
	while (i < 26)
	{
	if (i % 2 == 0)
	{	
		str[i] = j;
		j = j + 2;
	}
	else if (i % 2 == 1)
	{	
		str[i] = g;
		g = g + 2;
	}
		write(1, &str[i], 1);
		i++;
	}
	write(1, "\n",1);
	return (0);
}
