#include <unistd.h>

int	main(int ac, char **av)
{
	if (ac == 2)
	{
		int i = 0;

		while (av[1][i] != '\0')
			   i++;
		   if (av[1][i] != 'z')
				write(1, "z", 1);
	}
	else
		write(1, "b", 1);
	write(1, "\n", 1);
	return (0);
}

